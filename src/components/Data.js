class Data extends Component {
  state = {
    ready: false,
		search: '',
    columns:[
      {
        Header: 'Name',
        accessor: 'name'
      },
      {
        Header: 'Last',
        accessor: 'last'
      },
      {
        Header: 'High',
        accessor: 'high'
      },
      {
        Header: 'Low',
        accessor: 'low'
      },
      {
        Header: 'Volume',
        accessor: 'volume'
      }
    ],
    data: [],
    selectedPageSize: 10,
		pageSizes: [10,100],
		currentPage: 1,
    filtered: []
  }

	changePageSize(size) {
	 this.setState({ selectedPageSize: size, currentPage: 1});
 }

  componentDidMount() {
   this.getData()
  }



  render() {
		let data = this.state.data
		if (this.state.search) {
			data = data.filter(row => {
				return row.name.toLowerCase().includes(this.state.search.toLowerCase()) || row.last.includes(this.state.search) || String(row.volume).includes(this.state.search)
			})
		}
		let found = false
		for(let i = 0; i<data.length; i++){
			if ( data[i].name === 'RVN') {
				found = true
				const temp = data[i]
				data.splice(i, 1);
				data.unshift(temp)
				break;
			}
		}
		if(!found) {
			const datas = this.state.data
			for(let i=0; i<datas.length; i++) {
				if(datas[i].name === 'RVN') {
					data.unshift(datas[i])
					break;
				}
			}
		}

    return(
      <div>
			<span className="text-muted text-small mr-1"> Items per page: </span>
				<UncontrolledDropdown className="d-inline-block">
					<DropdownToggle caret color="outline-dark" size="xs">
						{this.state.selectedPageSize}
					</DropdownToggle>
					<DropdownMenu right>
						{this.state.pageSizes.map((size, index) => {
							return (
									<DropdownItem
											key={index}
											onClick={() => this.changePageSize(size)}
									>
										{size}
									</DropdownItem>
							);
						})}
					</DropdownMenu>
				</UncontrolledDropdown>
			<h4>Search: <input
						value={this.state.search}
						onChange={e => this.setState({search: e.target.value})}
					/></h4>
      <ReactTable
          data={data}
          columns={this.state.columns}
          defaultPageSize={this.state.selectedPageSize}
					filterable={true}
					className="-striped -highlight"

        />
      </div>
    )
  }

  getData() {
    const coins = json.data
    let data = []
    for(let i=0; i<coins.length; i++) {
      data.push({
        name: coins[i][1].toUpperCase(),
        last: coins[i][4],
        high: coins[i][5],
        low: coins[i][6],
        volume: coins[i][7]
      })
    }
    this.setState({data})
  }


}

